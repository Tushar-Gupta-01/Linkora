package com.example.linkora_1;

public class Suggestions {
    String cid,s;

    public Suggestions(String cid, String s) {
        this.cid = cid;
        this.s = s;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}
